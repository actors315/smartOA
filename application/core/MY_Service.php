class MY_Service {

	/**
	 * Constructor
	 *
	 * @access public
	 */
	function __construct()
	{
		log_message('info', "Service Class Initialized");
	}

	/**
	 * __get
	 *
	 * Allows Services to access CI's loaded classes using the same
	 * syntax as controllers.
	 *
	 * @param	string
	 * @access private
	 */
	function __get($key)
	{
		$CI =& get_instance();
		return $CI->$key;
	}
}
